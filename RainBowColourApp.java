package com.aj.selection;

public class RainBowColourApp {

	public void findRainBowColour(char colour) {
		if(colour=='V'||colour=='v') {
			System.out.println("Voilet Is Rainbow Colour");
		}else if(colour=='I'||colour=='i') {
			System.out.println("Indigo Is Rainbow Colour");
		}else if(colour=='B'||colour=='b') {
			System.out.println("Blue Is Rainbow Colour");
		}else if(colour=='G'||colour=='g') {
			System.out.println("Green Is Rainbow Colour");
		}else if(colour=='Y'||colour=='y') {
			System.out.println("Yellow Is Rainbow Colour");
		}else if(colour=='O'||colour=='o') {
			System.out.println("Orange Is Rainbow Colour");
		}else if(colour=='R'||colour=='r') {
			System.out.println("Red Is Rainbow Colour");
		}else {
			System.out.println("This Is Not Rainbow Colour");
		}
	}
}
